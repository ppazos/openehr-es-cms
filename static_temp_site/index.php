                    <h6>El aporte de openEHR</h6>
                  </div>
                  <p>
                    openEHR define un conjunto de componentes arquitect&oacute;nicos a modo de plano general de cualquier sistema de informaci&oacute;n en salud, donde cada uno de estos componentes cumple un rol importante y la falta de estos se har&aacute; notar en el futuro, cosa que ocurre con frecuencia en los dise&ntilde;os actuales que no est&aacute;n basados en est&aacute;ndares ni buenas pr&aacute;cticas. Es importante considerar que esta arquitectura fue dise&ntilde;ada, mejorada y estandarizda desde 1988, con diversos proyectos de la Uni&oacute;n Europea, que derivaron en la creaci&oacute;n de openEHR en 2002. Y desde esa fecha hasta ahora openEHR ha tenido varias mejoras y versiones de las especificaciones.
                  </p>
                  <p>
                    Por otro lado, openEHR define un modelo de informaci&oacute;n completo, expresivo, bien organizado, y estable, que adem&aacute;s permite ser mapeado con otros est&aacute;ndares, como est&aacute;ndares de mensajer&iacute;a HL7. En comparaci&oacute;n con los est&aacute;ndares HL7, openEHR est&aacute; enfocado en la interna de los sistemas de informaci&oacute;n, en c&oacute;mo la informaci&oacute;n cl&iacute;nica es modelada, almacenada y accedida, mientras que HL7 est&aacute; enfocado en el intercambio. Esto hace que ambos est&aacute;ndares sean complementarios y aliados en la implementaci&oacute;n de sistemas de informaci&oacute;n en salud.
                  </p>
                  <p>
                    En openEHR toda la informaci&oacute;n cl&iacute;nica est&aacute; definida mediante una metodolog&iacute;a formal, y basada en el modelo de informaci&oacute;n de openEHR. Cada estructura de datos debe ser definida como un arquetipo. Los arquetipos no son m&aacute;s que definiciones de conceptos, que incluyen estructura de datos, definiciones textuales, restricciones y codificaci&oacute;n. Por ejemplo, la presi&oacute;n arterial, frecuencia respiratoria, diagn&oacute;stico, resultados de estudios de laboratorio, triage, etc., cada uno de estos conceptos ser&aacute; definido con un arquetipo. Luego tenemos las plantillas que son contenedores de muchos arquetipos, conformando una definici&oacute;n de documentos cl&iacute;nicos, de los que participar&aacute;n los conceptos representados mediante arquetipos. No es necesario crear todos los arquetipos desde cero para cada proyecto, contamos con un repositorio internacional de arquetipos donde profesionales de todo el mundo comparten, validan y traducen arquetipos. Estos arquetipos son de uso libre y gratuito.
                  </p>
                  <p>
                    La comunidad tambi&eacute;n provee herramientas de software para trabajar con plantillas y arquetipos. Algunos son desarrollos comerciales, otros de uso gratuito, y otros de c&oacute;digo abierto. Los arquetipos y las plantillas se usan con otros fines m&aacute;s all&aacute; de la definici&oacute;n sem&aacute;ntica de conceptos cl&iacute;nicos, por ejemplo se pueden generar formularios de registro desde una plantilla, se pueden crear consultas de datos, tambi&eacute;n validar datos desde una plantilla, para mapear datos hacia diversos est&aacute;ndares y formatos, etc. Como son conjuntos de meta-datos, pueden ser utilizados con distintos fines.
                  </p>
