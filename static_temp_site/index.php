                    <h6>Qu&eacute; es y qu&eacute; no es openEHR</h6>
                  </div>
                  <ul>
                    <li>openEHR es un est&aacute;ndar abierto: generado, mantenido, probado, validado y accesible por una comunidad internacional de profesionales, tanto del &aacute;rea m&eacute;dica como del &aacute;rea inform&aacute;tica.</li>
                    <li>openEHR es un est&aacute;ndar, formado por especificaciones formales.</li>
                    <li>openEHR especifica una arquitectura para sistemas de informaci&oacute;n en salud.</li>
                    <li>La arquitectura propuesta por openEHR tiene en consideraci&oacute;n aspectos de estandarizaci&oacute;n de la informaci&oacute;n, del conocimiento, de los componentes del sistema, y de los servicios provistos por dichos componentes.</li>
                    <li>openEHR propone crear sistemas normalizados, flexibles a nuevos requerimientos, facilmente modificables, econ&oacute;micamente viables a largo plazo, durables en el tiempo.</li>
                    <li>openEHR permite la independencia de tecnolog&iacute;as y proveedores, pudiendo ser implementado, y actualmente lo es, en diversas tecnolog&iacute;as.</li>
                    <li>openEHR busca la longitudinalidad de la informaci&oacute;n cl&iacute;nica (que acompa&ntilde;e al paciente durante toda su vida) y la transversalidad entre distintos actores del sistema sanitario (informaci&oacute;n estandarizada capaz de ser compartida para evitar la fragmentaci&oacute;n del registro cl&iacute;nico).</li>
                    <li>openEHR propone la Historia Cl&iacute;nica Electr&oacute;nica virtual, capaz de permitir el acceso homog&eacute;neo a toda la informaci&oacute;n cl&iacute;nica de cualquier paciente, de forma independiente a la instituci&oacute;n sanitaria, hospital, cl&iacute;nica, etc. donde se haya generado dicha informaci&oacute;n.</li>
                    <li>openEHR propone mejorar el proceso de desarrollo de los sistemas de informaci&oacute;n en salud, orient&aacute;ndolo a la gesti&oacute;n del conocimiento cl&iacute;nicos por parte de expertos del dominio (no inform&aacute;ticos).</li>
                  </ul>
                  <ul>
                    <li>openEHR NO es un sistema/software, es un est&aacute;ndar, un conjunto de especificaciones y gu&iacute;as que sirven para construir sistemas de forma estandarizada.</li>
                    <li>openEHR NO es un est&aacute;ndar para mensajer&iacute;a de datos cl&iacute;nicos entre sistemas inform&aacute;ticos.</li>
                    <li>openEHR NO es comparable con HL7, son est&aacute;ndares complementarios que resuelven problemas distintos.</li>
                  </ul>
